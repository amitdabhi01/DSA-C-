#include<iostream>

using namespace std;

int main(){

    // for lopp

    for(int i=1; i<=10; i++){
        cout << i << endl;
    }

    cout << endl;

    for(int j=10; j>=1; j--){
        cout << j << endl;
    }





    // while lopp


    int a =1;

    while (a<=6)
    {
        cout << a << " ";
        a++;
    }

    int v =6;

    while (v>=0)
    {
        cout << v << " ";
        v--;
    }
    



    return 0;
}